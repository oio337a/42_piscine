int	add(int a, int b)
{
	return (a + b);
}

int	miner(int a, int b)
{
	return (a - b);
}

int	mul(int a, int b)
{
	return (a * b);
}

int	div(int a, int b)
{
	return (a / b);
}

int	modul(int a, int b)
{
	return (a % b);
}
