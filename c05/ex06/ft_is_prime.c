int	ft_is_prime(int nb)
{
	long	i;
	long	n;

	i = 2;
	n = nb;
	if (n <= 1)
		return (0);
	while (i * i <= n)
	{
		if (n % i == 0)
			return (0);
		i++;
	}
	return (1);
}
